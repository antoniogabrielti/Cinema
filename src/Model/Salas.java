package Model;

public class Salas {
   private int numero;
   private int capacidade;

    public Salas(int numero, int capacidade) {
        this.numero = numero;
        this.capacidade = capacidade;
    }

    public int getNumero() {
        return numero;
    }

    public int getCapacidade() {
        return capacidade;
    }
 
}